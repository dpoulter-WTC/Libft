#include <libft.h>

char	* ft_strtrim(char const *s)
{
	int		i;
	int		j;

	if (!s)
		return (NULL);
	j = ft_strlen(s) - 1;
	while (IS_SPACE(*(s + i)))
		i++;
	
}
